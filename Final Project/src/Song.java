public class Song {
	public String song;
	public String artist;
	public String album;
	public String genre;
	public int year;
	
	public Song(String song, String artist, String album, String genre, int year) {
		this.song = song;
		this.artist = artist;
		this.album = album;
		this.genre = genre;
		this.year = year;
	}
}
